package com.leizhou.dto;

public class RolePolicyBean {
	private int id;
	private String role_id;
	private String policy_id;
	private String role_uuid;
	private String policy_uuid;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRole_id() {
		return role_id;
	}
	public void setRole_id(String role_id) {
		this.role_id = role_id;
	}
	public String getPolicy_id() {
		return policy_id;
	}
	public void setPolicy_id(String policy_id) {
		this.policy_id = policy_id;
	}
	public String getRole_uuid() {
		return role_uuid;
	}
	public void setRole_uuid(String role_uuid) {
		this.role_uuid = role_uuid;
	}
	public String getPolicy_uuid() {
		return policy_uuid;
	}
	public void setPolicy_uuid(String policy_uuid) {
		this.policy_uuid = policy_uuid;
	}
	
	
}
